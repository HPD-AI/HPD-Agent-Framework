/**
 * Shared type definitions for HPD Headless UI
 */

import type { HTMLAttributes } from 'svelte/elements';

/**
 * Standard HTML div attributes for primitive components
 */
export type HPDPrimitiveDivAttributes = HTMLAttributes<HTMLDivElement>;

/**
 * Standard HTML button attributes for primitive components
 */
export type HPDPrimitiveButtonAttributes = HTMLAttributes<HTMLButtonElement>;

/**
 * Standard HTML heading attributes for primitive components
 */
export type HPDPrimitiveHeadingAttributes = HTMLAttributes<HTMLHeadingElement>;
